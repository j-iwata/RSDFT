  SUBROUTINE construct_gaunt_coef_L1( L, yyy )
    implicit none
    integer,intent(IN) :: L
    type(gaunt),intent(INOUT) :: yyy
    if ( allocated(yyy%x) ) then
       if ( L > yyy%L ) then
          deallocate( yyy%x )
          deallocate( yyy%y )
          deallocate( yyy%z )
       else
          return
       end if
    end if
    yyy%L = L
    allocate( yyy%x(0:L,-L:L,0:L+1,-L-1:L+1) ) ; yyy%x=0.0d0
    allocate( yyy%y(0:L,-L:L,0:L+1,-L-1:L+1) ) ; yyy%y=0.0d0
    allocate( yyy%z(0:L,-L:L,0:L+1,-L-1:L+1) ) ; yyy%z=0.0d0
    call get_gaunt_coefficient_L1( L, yyy%x, yyy%y, yyy%z ) 
  END SUBROUTINE construct_gaunt_coef_L1


  SUBROUTINE construct_gaunt_coef_LL( L1, L2, GCLL )
    implicit none
    integer :: L1, L2
    type(gaunt_ll),intent(INOUT) :: GCLL
    if ( allocated(GCLL%yyy) ) then
       if ( L1 > GCLL%L1 .or. L2 > GCLL%L2 ) then
          deallocate( GCLL%yyy )
       else
          return
       end if
    end if
    GCLL%L1 = L1
    GCLL%L2 = L2
    allocate( GCLL%yyy(0:L1,-L1:L1,0:L2,-L2:L2,0:L1+L2,-L1-L2:L1+L2) )
    GCLL%yyy=0.0d0
    call get_gaunt_coefficient_LL( L1, L2, GCLL%yyy )
